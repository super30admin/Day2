class Node:
    def __init__(self, key=None, val=None):
        self.key = key
        self.val = val
        self.next = None


class MyHashMap:
    def __init__(self):
        self.storage = [None for i in range(10000)]

    def hash(self, key) -> int:
        return key % 10000

    def search(self, prev: Node, key: int):
        curr = Node()
        curr = prev.next
        while curr is not None:
            if curr.key == key:
                return prev
            prev = curr
            curr = curr.next
        return prev

    def put(self, key: int, value: int) -> None:
        Pindex = self.hash(key)
        if self.storage[Pindex] == None:
            self.storage[Pindex] = Node(-1, -1)
        prev = self.search(self.storage[Pindex], key)
        if prev.next == None:
            prev.next = Node(key, value)
        else:
            prev.next.val = value
        # print("Put: " + key + "_" + value)
        return

    def get(self, key: int) -> int:
        try:
            idx = self.hash(key)
            if self.storage[idx] == None:
                # print("get: " + key)
                return -1
            prev = self.search(self.storage[idx], key)
            if prev.next == None:
                # print("get: " + key)
                return -1
            # print("get: " + key)
            return prev.next.val
        except:
            # print("get: " + key)
            return -1

    def remove(self, key: int) -> None:
        idx = self.hash(key)
        # print("Hash Key:" + str(key))
        # print("Hash:" + str(idx))
        # print("Len of Storage: " + str(len(self.storage)))
        if self.storage[idx] == None:
            # print("Removed: " + str(key))
            return
        prev = self.search(self.storage[idx], key)
        if prev.next == None:
            # print("Removed: " + key)
            return
        temp = prev.next
        prev.next = prev.next.next
        temp.none = None
        # print("Removed: " + key)
        return


